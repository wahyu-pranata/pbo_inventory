    public void clearField()
    {
        selectedId = -1;
        nameField.setText("");
        stockField.setText("");
        unitField.setText("");
        typeField.setText("");
    }
    
    public boolean isFieldEmpty()
    {
        if(nameField.getText().length() == 0
           || stockField.getText().length() == 0
           || unitField.getText().length() == 0
           || typeField.getText().length() == 0) {
            return true;
        }
        return false;
    }

