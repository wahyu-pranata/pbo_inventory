    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        selectedId = (int) table.getValueAt(table.getSelectedRow(), 0);
        Object selectedNama = table.getValueAt(table.getSelectedRow(), 1);
        Object selectedStock = table.getValueAt(table.getSelectedRow(), 2);
        Object selectedUnit = table.getValueAt(table.getSelectedRow(), 3);
        Object selectedType = table.getValueAt(table.getSelectedRow(), 4);
        
        informationText.setText("Selected id = " + selectedId);
        
        nameField.setText(String.valueOf(selectedNama));
        stockField.setText(String.valueOf(selectedStock));
        unitField.setText(String.valueOf(selectedUnit));
        typeField.setText(String.valueOf(selectedType));
    }//GEN-LAST:event_tableMouseClicked

    private void addButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addButtonMouseClicked
       try {
            if(isFieldEmpty()) {
                JOptionPane.showMessageDialog(null, "Harap isi seluruh field", "Peringatan", JOptionPane.ERROR_MESSAGE);
                return;
            }
            boolean isInsertSuccess = ItemController.insertItem(nameField.getText(), Integer.valueOf(stockField.getText()), unitField.getText(), typeField.getText());
            if(isInsertSuccess) {
                JOptionPane.showMessageDialog(null, "Berhasil menambah data", "Informasi", JOptionPane.INFORMATION_MESSAGE);
                this.loadData();
                return;
            }
            JOptionPane.showMessageDialog(null, "Gagal menambah data", "Informasi", JOptionPane.INFORMATION_MESSAGE);
            return;
       } catch(Exception e) {
           System.out.println("Something went wrong\n" + e.getMessage());
       }
    }//GEN-LAST:event_addButtonMouseClicked

